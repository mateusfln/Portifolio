#!/bin/sh           //  serve como inicializador do codigo
 
INPUT_STRING=”hello” //  serve para igualar a variavel a palavra hello

while [[ $INPUT_STRING != "bye" ]] //  condicao de comando

do // serve para realizar os comandos abaixo

echo "Please type something in (bye to quit)" //  serve para exibir na tela a frase escrita
 
read INPUT_STRING //   serve para ler a variavel citada

echo "You typed: $INPUT_STRING" //  serve para exibir na tela a frase escrita
 
done // serve para finalizar os comandos
